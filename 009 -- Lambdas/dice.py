class Die():
    def __init__(self,sideCount):
        self.sideCount = sideCount
    def roll(self):
        return randint(1,self.sideCount)
    
def numInput(text,min=0,max=100):
    output = ''
    while True:
        output = input(text)
        if output.isnumeric():
            if min <= int(output) <= max:
                break
    return int(output)
def setup():
    global diceCount, dice
    diceCount = numInput('Enter the number of dice you want to use: ',0,20)
    for i in range(0,diceCount):
        dice.append(Die(numInput('Enter the number of sides for dice '+str(i+1)+':',2,10)))
        
def roll():
    global dice
    print('nnn'+'='*20)
    for die in dice:
        print(sides[die.roll()])
dice = []
diceCount = 0
setup()
