#Integer Math
x = 1 + 2
print(x)
print(type(x))
x = 1 - 2
print(x)
print(type(x))
x = 1 * 2
print(x)
print(type(x))

#Floating Point Math
x = 1.1 + 2.2
print(x)
print(type(x))
x = 1.0 - 2.2
print(x)
print(type(x))
x = 2.1 * 2.1
print(x)
print(type(x))


#Floating Point mixed with Integer Math
x = 1 + 2.2
print(x)
print(type(x))
x = 1 - 2.2
print(x)
print(type(x))
x = 2 * 2.1
print(x)
print(type(x))