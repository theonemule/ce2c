#Integer Division
x = 5 // 2
print(x)
print(type(x))

x = -5 // 2
print(x)
print(type(x))

x = 5.0 // 2.0
print(x)
print(type(x))

#Floating Point Division
x = 5 / 2
print(x)
print(type(x))

x = -5 / 2
print(x)
print(type(x))

x = 5.0 / 2.0
print(x)
print(type(x))

x = 4 / 2
print(x)
print(type(x))

#Modulus -- "Remainder"

x=5%2
print(x)
print(type(x))

x=4%2
print(x)
print(type(x))

x=5.0%2.4
print(x)
print(type(x))


